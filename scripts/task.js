class Task{
    constructor(important, title, description, dueDate, taskLocation, invites, color, frequency, status){
        this.important = important;
        this.title = title;
        this.description = description;
        this.dueDate = dueDate;
        this.taskLocation = taskLocation;
        this.invites = invites;
        this.color = color;
        this.frequency = frequency;
        this.status = status;
    }
}